public class GameCharacter{

    private int skill, stamina;

    public int getSkill(){
        return skill;
    }
    public void setSkill(int skill){
        this.skill = skill;
    }

    public int getStamina(){
        return stamina;
    }
    public void setStamina(int stamina){
        this.stamina = stamina;
    }
}